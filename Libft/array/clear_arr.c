#include "libft.h"

void	clear_arr(char **arr)
{
	int	i;

	i = 0;
	if (!arr) return ;
	while (arr[i])
	{
		if (arr[i])
			free(arr[i]);
		i++;
	}
	if (arr)
		free(arr);
}
